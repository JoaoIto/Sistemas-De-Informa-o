package classes;

public class matematica {

    public int soma(double n1, double n2){
        return (int) (n1 + n2);
    }

    public int soma(double n1, int n2){
        return (int) (n1 + n2);
    }

    public int soma(int n1, double n2){
        return (int) (n1 + n2);
    }

    public int subrai(double n1, double n2){
        return (int) (n1 - n2);
    }

    public int subrai(int n1, double n2){
        return (int) (n1 - n2);
    }

    public int subrai(double n1, int n2){
        return (int) (n1 - n2);
    }

    public double multiplica(double n1, double n2){
        return (int) (n1 * n2);
    }

    public double multiplica(int n1, double n2){
        return (int) (n1 * n2);
    }

    public double multiplica(double n1, int n2){
        return (int) (n1 * n2);
    }

    public double divide(double n1, double n2){
        return n1 / n2;
    }
}
