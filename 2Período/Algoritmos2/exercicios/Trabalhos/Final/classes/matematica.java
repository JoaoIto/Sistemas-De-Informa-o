package classes;

public class matematica {

    private double n1;
    private double n2;
    private int result;

    public double soma(double n1, double n2){
        return n1 + n2;
    }

    public double subrai(double n1, double n2){
        return n1 - n2;
    }

    public double multiplica(double n1, double n2){
        return n1 * n2;
    }

    public double divide(double n1, double n2){
        return n1 / n2;
    }
}
